static int Run(ICommand command)
{
    try
    {
        var task = command.Run();
        task.Wait();
        return 0;
    }
    catch
    {
        return 1;
    }
}

static int ShowError(IEnumerable<Error> errs)
{
    if (errs.Where(err => err is not HelpVerbRequestedError && err is not VersionRequestedError).Any())
    {
        ShowBot("That did not work.");
        return 1;
    }

    return 0;
}

Parser.Default.ParseArguments<CreateEmulatorOptions, PrefillCacheOptions, PublishMicrofrontendOptions>(args)
    .MapResult(
        (CreateEmulatorOptions opts) => Run(opts),
        (PrefillCacheOptions opts) => Run(opts),
        (PublishMicrofrontendOptions opts) => Run(opts),
        ShowError
    );
